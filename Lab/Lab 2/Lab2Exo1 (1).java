package src;

public class Main {
	/**
	 * Méthode pilote permettant l'affichage de la liste
	 * 
	 * @param args Arguments du programme. Aucun n'est requis pour celui-ci.
	 */
	public static void main(String[] args) {

		/*
		 * Les prochaîne lignes affichent une liste.
		 */
		System.out.print("\r");
		System.out.print("Liste d'élèves :\r");
		System.out.print("\r");
		System.out.print("==================================================\r");
		System.out.print("\tNom\t\tPrénom\r"); // entête
		System.out.print("\t====\t\t========\r");
		System.out.print("\tBéliveau\tCatherine\r"); // étudiants
		System.out.print("\tDesjardins\tMathieu\r");
		System.out.print("===================================================\r");
	}

}
